package Operators;

public class Bitwise {
    public static void main(String[] args) {
        int a = 2;
        int b = 5;
        System.out.println("a&b=" + (0));
        System.out.println("a|b=" + (a | b));
        System.out.println("a^b=" + (a ^ b));
        System.out.println("~a=" + (~a));
        System.out.println("a=" + a);

    }
}
