package Operators;

public class ArithmeticOperators {
    public static void main(String[] args) {

        int num1=12;
        int num2=8;
        int sum=num1+num2;
        int diff=num1-num2;
        int product=num1*num2;
        int div=num1/num2;

        System.out.println(sum);
        System.out.println(diff);
        System.out.println(product);
        System.out.println(div);
    }
}

































































































































































































































































































































































































































































































