package Operators;

import java.util.Scanner;

public class RelationalOperators {
    public static void main(String[] args) {

        int n1= 20;
        int n2=30;


        System.out.println("n1 > n2 is : " +(n1>n2));
        System.out.println("n1 < n2 is : " +(n1<n2));
        System.out.println("n1 >= n2 is : " +(n1>=n2));
        System.out.println("n1 <= n2 is : " +(n1<=n2));
        System.out.println("n1 == n2 is : " +(n1==n2));
        System.out.println("n1 != n2 is : " +(n1!=n2));


    }

}
